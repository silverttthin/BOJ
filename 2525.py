'''
입력:
첫째줄에 시작시각
둘째줄에 걸리는 시간

23을 넘어서 24가 되면
시는 00으로 처리해야함

분이 60이 넘으면 
해당 분 =- 60 and 시 += 1

'''
h, m = 23,59
duration = 100
m += duration
if m>= 60 :
    h = h + (m//60)
    m %= 60
if h >= 24: h -= 24

print(h, m)

# h가 24 초과하였다고 명시해야했는데 h == 24라고 해놔서 h가 25인 경우
# 가 제대로 안됐음. 또한 h = 0이라 해서 제대로 h 연산이 안됐음


